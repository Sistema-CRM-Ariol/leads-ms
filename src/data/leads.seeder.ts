const START_DATE = new Date("2025-12-01T08:00:00.000Z").getTime();
const END_DATE = new Date("2026-03-23T22:30:00.000Z").getTime();
const TOTAL_LEADS = 60;

const rangeMs = END_DATE - START_DATE;

export const leadsSeeder: Prisma.LeadsCreateManyInput[] = Array.from({ length: TOTAL_LEADS }, (_, index) => {
    const base = leadTemplates[index % leadTemplates.length];
    const indexPad = String(index + 1).padStart(3, "0");
    const normalizedName = base.name.toLowerCase().replace(/\s+/g, ".");
    const normalizedLastname = base.lastname.toLowerCase().replace(/\s+/g, ".");

    return {
        name: base.name,
        lastname: `${base.lastname} ${indexPad}`,
        company: index % 4 === 0 ? null : `Empresa ${indexPad}`,
        position: base.position,
        city: base.city,
        address: `${base.address}, Oficina ${((index % 12) + 1)}`,
        razonSocial: index % 3 === 0 ? null : `RS-${indexPad}`,
        nit: `10${String(70000000 + index).padStart(8, "0")}`,
        email1: `${normalizedName}.${normalizedLastname}${indexPad}@${base.domain}`,
        email2: index % 2 === 0 ? null : `${normalizedName}${indexPad}@mail.com`,
        phone1: `+5917${String(1000000 + index).padStart(7, "0")}`,
        phone2: index % 2 === 0 ? null : `+5916${String(1000000 + index).padStart(7, "0")}`,
        source: base.source,
        status: base.status,
        priority: base.priority,
        description: `Lead generado para pruebas (${indexPad}) en ${base.city}.`,
        notes: `Seguimiento automatico del lead ${indexPad}.`,
        isActive: index % 9 !== 0,
        createdAt: new Date(START_DATE + Math.floor((rangeMs * index) / (TOTAL_LEADS - 1))),
    };
});
